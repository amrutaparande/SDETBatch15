package class7;

public class ForLoops {
    public static void main(String[] args) {
        /*prints number from0-9*/
        int number=0;
        while(number<10){
            System.out.println(number);
            number++;
        }
        /* (initialize;condition;increamentor decreament)*/
        for(int i=0;i<10;i++){
            System.out.println(i);
        }
    }
}
